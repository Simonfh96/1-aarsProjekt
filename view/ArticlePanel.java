    private void formMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_formMousePressed
         if (selected == false) {
            for (Component articlePanel : getParent().getComponents()) {
                ArticlePanel ap = (ArticlePanel) articlePanel;
                if (ap.isSelected()) {
                    ap.setSelected(false);
                    ap.setBackground(normal);
                }
            }
            setBackground(Color.BLUE);
            selected = true;
            articleDescription.setText(a.getDescription());
            ((DefaultListModel)taskList.getModel()).clear();
            if (a.getTasks().size() > 0) {
            for (Task t : a.getTasks()) {
                System.out.println(t.toString());
                ((DefaultListModel)taskList.getModel()).addElement(t);
                taskList.repaint();
                taskList.revalidate();
            }
            }
        } else {
            setBackground(normal);
            selected = false;
        }
    }//GEN-LAST:event_formMousePressed


